package hfsguardadir.objetos;

public class Importar {
    private int aba;		
    private int codDirRaiz;
    private String rotuloRaiz;
    private String nomeDirRaiz;    	
    private String caminho;    

    public int getAba() {
        return aba;
    }

    public void setAba(int aba) {
        this.aba = aba;
    }

    public int getCodDirRaiz() {
        return codDirRaiz;
    }

    public void setCodDirRaiz(int codDirRaiz) {
        this.codDirRaiz = codDirRaiz;
    }

    public String getRotuloRaiz() {
        return rotuloRaiz;
    }

    public void setRotuloRaiz(String rotuloRaiz) {
        this.rotuloRaiz = rotuloRaiz;
    }

    public String getNomeDirRaiz() {
        return nomeDirRaiz;
    }

    public void setNomeDirRaiz(String nomeDirRaiz) {
        this.nomeDirRaiz = nomeDirRaiz;
    }

    public String getCaminho() {
        return caminho;
    }

    public void setCaminho(String caminho) {
        this.caminho = caminho;
    }
}
